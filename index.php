            <h2>Citizens Making a <pre>DIFF</pre> in Legislation</h2>
        </header>
        <div class="content">
            <p><span class="firstcharacter">L</span>aws have been written for centuries. They both define our history and set the course for our future. With so much at stake in codifying our daily lives, it is distressing that we&#8217;ve allowed ourselves to be so far removed from the process.</p>
            <p>Legistextura aims to improve our system in two areas; discovery and comprehension for existing legislation, and awareness and participation for proposed legislation.</p>
            <p>This endeavor is non-partisan since the end result is mutually beneficial, regardless of ideology. There exists no logical argument for obscuring or impeding access to any part of the legislative process.</p>
            <h3>Early Goals:</h3>
            <ul>
                <li>Raise awareness of open source codification</li>
                <li>Define a mission statement</li>
                <li>Locate individuals who are willing to donate time to discussion and/or code</li>
                <li>Reach out to current legislators (city/state/federal) who can represent our ambitions fairly and accurately to their peers</li>
            </ul>
            <p>We hope you will share your time, knowledge and experience with us in seeking a framework that will help bring the power of <a href="http://git-scm.com/">Git</a> to our legislation.</p>
